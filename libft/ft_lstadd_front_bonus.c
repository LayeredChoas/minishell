#include "libft.h"

void	ft_lstadd_front(t_list **alst, t_list *new)
{
	if (new && *alst)
	{
		new->next = *alst;
		*alst = new;
	}
	if (!*alst && new)
	{
		new->next = NULL;
		*alst = new;
	}
}
